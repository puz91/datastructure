#include<iostream>
#include<algorithm>
using namespace std;

int main(){
    int n, max;  
    max = -1000;  // กำหนดค่า max เริ่มต้นที่ -1000 เพื่อเปรียบเทียบค่าที่สูงสุดในตาราง
    cin >> n;     // อ่านขนาดของตาราง (n x n)
    int a[n][n];  // สร้างตาราง a ขนาด n x n
    
    // ลูปผ่านทุกตำแหน่งในตาราง
    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            cin >> a[i][j];  // อ่านค่าของแต่ละตำแหน่งในตาราง
           
            // เช็คว่าถ้า i, j อยู่ในขอบเขตที่สามารถเข้าถึงตำแหน่ง a[i-1][j-1] ได้ (เช่น i > 0, j > 0)
            // แล้วคำนวณผลรวมของค่าในตำแหน่ง (i, j) กับ (i-1, j-1) ถ้าผลรวมมากกว่าค่าปัจจุบันที่ a[i][j]
            if(i > 0 && j > 0){
                if(a[i][j] + a[i-1][j-1] > a[i][j]){
                    a[i][j] = a[i][j] + a[i-1][j-1];  // อัปเดตค่าของ a[i][j] ถ้าผลรวมมากกว่า
                }
            }
            
            // ตรวจสอบหาค่าสูงสุดในตาราง
            if(a[i][j] > max){
                max = a[i][j];  // ถ้าค่าของ a[i][j] มากกว่า max ให้ปรับค่า max
            }
        }
    }
    
    cout << max;  // พิมพ์ค่าที่สูงสุด
}
